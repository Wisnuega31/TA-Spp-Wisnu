    private void loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginMouseClicked
        try {
            // TODO add your handling code here:
            KoneksiDb konn = new KoneksiDb();
            String sql = "select * from petugas where username = '" + username.getText() + "' and password = '" + password.getText() + "'";
            ResultSet rs = konn.ambilData(sql);

            if (rs.next()) {
                if (rs.getString("level").equals("petugas")) {
                    Petugas.setNama_petugas(rs.getString("nama_petugas"));
                    MDI_Petugas utama = new MDI_Petugas();
                    utama.setVisible(true);
                    this.setVisible(false);
                } else if (rs.getString("level").equals("admin")) {
                    Petugas.setNama_petugas(rs.getString("nama_petugas"));
                    MDI_Admin utama = new MDI_Admin();
                    utama.setVisible(true);
                    this.setVisible(false);                   
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "user tidak ditemukan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Login gagal");
        }
    }//GEN-LAST:event_loginMouseClicked

