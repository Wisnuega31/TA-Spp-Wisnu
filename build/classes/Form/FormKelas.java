    private void muatTabel() {
        try {
            DefaultTableModel tb = (DefaultTableModel) Tabel.getModel();
            tb.getDataVector().removeAllElements();

            KoneksiDb konn = new KoneksiDb();
            String sql = "select * from kelas";
            ResultSet rs = konn.ambilData(sql);

            while (rs.next()) {
                Vector baris = new Vector();
                baris.add(rs.getInt("id_kelas"));
                baris.add(rs.getString("nama_kelas"));
                baris.add(rs.getString("kompetensi_keahlian"));
                tb.addRow(baris);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormKelas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

