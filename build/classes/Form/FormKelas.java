    private void simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simpanMouseClicked
        // TODO add your handling code here:
        String sql = "insert into kelas(nama_kelas,kompetensi_keahlian) values('" + kelas.getSelectedItem() + "','" + kompetensi.getSelectedItem()+ "')";

        KoneksiDb db = new KoneksiDb();
        if (db.rubahData(sql)) {
            JOptionPane.showMessageDialog(null, "data berhasil ditambahkan");
            muatTabel();
            kosongkan();
        } else {
            JOptionPane.showMessageDialog(null, "data gagal ditambahkan");
        }
    }//GEN-LAST:event_simpanMouseClicked

    private void TabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelMouseClicked
        // TODO add your handling code here:
        int baris = Tabel.getSelectedRow();
        kelas.setSelectedItem(Tabel.getValueAt(baris, 1).toString());
        kompetensi.setSelectedItem(Tabel.getValueAt(baris, 2).toString());
        
        simpan.setEnabled(false);
        ubah.setEnabled(true);
        hapus.setEnabled(true);
    }//GEN-LAST:event_TabelMouseClicked

    private void ubahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ubahMouseClicked
        // TODO add your handling code here:
        int baris = Tabel.getSelectedRow();
        String sql = "update kelas set nama_kelas = '"+kelas.getSelectedItem()+"', kompetensi_keahlian = '"+kompetensi.getSelectedItem()+"' where id_kelas = "+Tabel.getValueAt(baris, 0);

        KoneksiDb db = new KoneksiDb();
        if (db.rubahData(sql)) {
            JOptionPane.showMessageDialog(null, "data berhasil diubah");
            muatTabel();
            kosongkan();
        } else {
            JOptionPane.showMessageDialog(null, "data gagal diubah");
        }
        
    }//GEN-LAST:event_ubahMouseClicked

    private void hapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hapusMouseClicked
        // TODO add your handling code here:
        int baris = Tabel.getSelectedRow();
        String sql = "delete from kelas where id_kelas = "+Tabel.getValueAt(baris, 0);

        KoneksiDb db = new KoneksiDb();
        if (db.rubahData(sql)) {
            JOptionPane.showMessageDialog(null, "data berhasil dihapus");
            
            DefaultTableModel tb = (DefaultTableModel) Tabel.getModel();
            tb.removeRow(baris);
            Tabel.setModel(tb);
            
            kosongkan();
        } else {
            JOptionPane.showMessageDialog(null, "data gagal dihapus");
        }
    }//GEN-LAST:event_hapusMouseClicked

